package conc;

public class while_loop {


			public static void main(String[] args) {
//execute first the condition 
				int k=1;

				//syntax while(condition)

				while(k<15) {

					System.out.println(k);
					k++;
					k=k+2;

				}
				
				
				for(int j=20;j>0;j=j-5) {
					System.out.println(j);
				}
				
				
	}

}
